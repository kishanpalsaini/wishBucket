                        <h4 class="sub-heading sub-heading-color">What is your salary expectation?</h4>
                        <P>This question is asked at the beginning of the interview or at the end of the interview but
                            never answers the question directly. Otherwise, you will lose your job. Apart from salary,
                            there are quite a lot of benefits that are for you and that's the cost of the company.</P>
                        <P>Benefits like: vacation days, sick days, medical insurance, flexible hours, training, parking
                            or public transportation reimbursement, annual, quarterly, or other bonuses, etc. </P>
                        <p><i> Here are the tips that will encourage you to master better.</i></p>
                        <h5 class="tips">Tip No#1</h5>
                        <P>Salary is not the first priority for me right now before I talk about it, please tell me what
                            will be my job post and what will be my duties for that.  I would also like to know what is
                            expected of me while I work here. (After knowing the job post and your duty for it, talk
                            about salary) I am looking for a position that will pay me between 2 to 3 lakh per annum.
                        </P>
                        <h5 class="tips">Tip No#2</h5>
                        <P>Before answering this question, Can you please explain the opportunities for development in
                            this company? For my job status, I know very well that this company will give me a lot of
                            growth. And please share your budget for your employees because I do not have any salary
                            expectations as a fresher right now. I just want to increase my knowledge and skills.</P>

                    <h1 class="mb-5 main-heading"> Interview tips </h1>
                    <h4 class="sub-heading"> Understand the company and its need:</h4>
                    <p>Read about the company not just for job purpose but to the understanding purpose.</p>
                    <ul>
                        <li>Review the company mission and history.</li>
                        <li>Company product and service.</li>
                        <li>Company management and company culture.</li>
                    </ul>
                    <p>This information is available on the company website and browses their social accounts such as follow:</p>
                    <ul>
                        <li>Facebook</li>
                        <li>Instagram</li>
                        <li>Twitter</li>
                        <li>LinkedIn</li>
                        <li>Google+</li>
                        <li>Pinterest</li>
                    </ul>
                    
                    <h4 class="sub-heading"> Maintain your resume</h4>
                    <p>Now you know almost everything about the company now you can maintain your resume according to the job requirement and position. </p>
                    <p>You should understand exactly what they are looking for in new hire:</p>
                    <p>(Bring at least two copies of your resume to every interview.)</p>
                    <p>Prepare a response to expected interview questions.</p>
                    <p>Ask directly to the hiring manager</p>
                    <ul>
                        <li>Will it be one-on-one or in a group?</li>
                        <li>Will it be with one round or will you give several rounds of the interview?</li>
                    </ul>
                    
                    <h4 class="sub-heading">Practice, Practice, Practice and Practice.</h4>
                    <p>Think what makes you the best candidate for the position like you are good in teamwork, time management, problem-solving, work ethic so forth. Give some examples of your positive point.</p>
                    <ul>
                        <li>Prepare for common interview questions.</li>
                        <li>Line up your questions for the interview.</li>
                    </ul>
                    <p>Prepare your questions and answers so you won't have to fumble for them during the actual interview. No matter how clear your thoughts are in your own mind! Practice your question and answers 10 to 15 times, and you'll sound a lot smoother and more articulate.</p>
                    <p>Whatever you do, make sure your practice consists of speaking aloud.</p>
                    <p>Most important: Come in with energy she/he may be seeing a lot of other candidates that day and may be tired. So bring in that energy!</p>
                    
                    <h4 class="sub-heading">The first impression is the last impression</h4>
                    <ul>
                        <li>Dress for success. </li>
                        <li>Make eye contact. </li>
                        <li>Do a handshake.</li>
                        <li>Have good posture.</li>
                        <li>Speak clearly.</li>
                    </ul>
                    
                    <h4 class="sub-heading"> Send thank you email or note.</h4>
                    <p>Send a follow-up message to thank the interviewer, Mention something specific you talked about in the interview.</p>
                    <p>Express interest in the position and tell them you’re excited to learn more.</p>
                    <p>Interview thank you email sample you can use after the interview:</p>
                    <p>“Subject: Thank you for the interview</p>
                    <p>Hello</p>
                    <p>(Interviewer name),</p>
                    <p>I wanted to thank you for your precious time to speak with me (mention day here) about (job title) at (company name). I enjoyed our conversation about (a specific topic you discussed)</p>
                    <p>I am confident and excited about this opportunity to join. Please do not hesitate to email or call me if you have any questions or need any additional information.</p>
                    <p>Thank you again or the interview.</p>
                    <p>Best regards,</p>
                    <p>(Your name)”</p>
                    <p>Do not send exactly the same email/note to everyone who interviewed you.</p>
                    <p>Focus on sending the most professional thank you note/email good spelling and grammatically correct sentences, don't be too casual.</p>
